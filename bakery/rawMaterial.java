    private void MeasureInsertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MeasureInsertButtonActionPerformed
        // TODO add your handling code here:
        try{
            conn = ConnectMsSql.ConnectDB();
            Statement st=conn.createStatement();
            String txt = MeasureTxt.getText();
            int meas = 0;
            ResultSet measure = st.executeQuery("select ID from Units where Unit = '" + txt + "'");
            while(measure.next()){
                   meas = measure.getInt("ID");
            }

            String name=idtxt.getText();
            String name1=ProductName12.getText();
            double sum=Double.parseDouble(SumTxt.getText());
            double amount=Double.parseDouble(AmountTxt.getText());
            
            String dobavlenie = "INSERT INTO RawMaterials (Naimenovaniye_Syrya,Unit,Amount,Kol_Syrya) VALUES(?,?,?,?)";
            PreparedStatement pst=conn.prepareStatement(dobavlenie);

            pst.setString(1, name1);
            pst.setInt(2, meas);
            pst.setDouble(3, sum);
            pst.setDouble(4, amount);

            JOptionPane.showMessageDialog(null, "Данные успешно добавленны.");
            pst.executeUpdate();
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null, "Ошибка, что то пошло не так :(");
        }
    }//GEN-LAST:event_MeasureInsertButtonActionPerformed

